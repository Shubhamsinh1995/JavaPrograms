package javaInteviewQue;

public class SwapString1 {

	public static void main(String[] args) {
		
		String a="shubham";
		String b="Baiyas";
		String c;
		System.out.println("before swapping ");
		System.out.println("a="+ a+"    b= "+b);
		
		System.out.println("------------------------");
		c=a;
		a=b;
		b=c;
        System.out.println("After swapping");
        System.out.println("a="+ a+"    b= "+b);
	}

}
